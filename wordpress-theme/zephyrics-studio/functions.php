/**
 * Enqueue Scripts and Styles
 */
function zephyrics_enqueue_assets() {
    // Google Fonts
    wp_enqueue_style(
        'zephyrics-google-fonts',
        'https://fonts.googleapis.com/css2?family=Inter:wght@300;400;500;600;700;800;900&family=Space+Grotesk:wght@400;500;600;700&display=swap',
        array(),
        null
    );
    
    // Main stylesheet
    wp_enqueue_style(
        'zephyrics-style',
        get_stylesheet_uri(),
        array( 'zephyrics-google-fonts' ),
        ZEPHYRICS_VERSION
    );
    
    // Theme JavaScript
    wp_enqueue_script(
        'zephyrics-script',
        ZEPHYRICS_URI . '/assets/js/theme.js',
