/**
 * Add WhatsApp number setting
 */
function zephyrics_get_whatsapp_url() {
    $number = get_theme_mod( 'zephyrics_whatsapp_number', '1234567890' );
    $message = get_theme_mod( 'zephyrics_whatsapp_message', "Hi! I'm interested in your services." );
    return 'https://wa.me/' . preg_replace( '/[^0-9]/', '', $number ) . '?text=' . urlencode( $message );
}

/**
 * Disable emoji scripts for performance
 */
function zephyrics_disable_emojis() {
    remove_action( 'wp_head', 'print_emoji_detection_script', 7 );
    remove_action( 'admin_print_scripts', 'print_emoji_detection_script' );
    remove_action( 'wp_print_styles', 'print_emoji_styles' );
    remove_action( 'admin_print_styles', 'print_emoji_styles' );
    remove_filter( 'the_content_feed', 'wp_staticize_emoji' );
    remove_filter( 'comment_text_rss', 'wp_staticize_emoji' );
    remove_filter( 'wp_mail', 'wp_staticize_emoji_for_email' );
}
add_action( 'init', 'zephyrics_disable_emojis' );

